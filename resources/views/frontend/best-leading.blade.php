        $(document).ready(function() {
            var stripe = Stripe("{{ config('services.stripe.key') }}");
            var elements = stripe.elements();
            var card = elements.create('card', {
                style: {
                    base: {
                        fontSize: '16px',
                        color: '#32325d'
                    }
                }
            });
            card.mount('#card-element');

            card.on('change', function(event) {
                var displayError = document.getElementById('card-errors');
                if (event.error) {
                    displayError.textContent = event.error.message;
                } else {
                    displayError.textContent = '';
                }
            });

            // Handle the "Pay Now" button click
            const payNowButtons = document.querySelectorAll('.pay-now-btn');
            payNowButtons.forEach(button => {
                button.addEventListener('click', function() {
                    // Get package details from the clicked button
                    const packageName = this.getAttribute('data-package-name');
                    const packagePlan = this.getAttribute('data-package-plan');
                    const packagePrice = this.getAttribute('data-package-price');

                    // Populate modal fields with selected package info
                    document.getElementById('package').value = packageName;
                    document.getElementById('plan').value = packagePlan;
                    document.getElementById('amount').value = packagePrice;
                    document.getElementById('currency').value = "USD";

                    // Show the modal
                    const paymentModal = new bootstrap.Modal(document.getElementById(
                        'paymentModal'));
                    paymentModal.show();
                });
            });

            // Handle form submission for payment
            $("#paymentForm").on("submit", function(e) {
                e.preventDefault();

                const amount = parseFloat(document.getElementById('amount').value);
                if (amount < 0.50) {
                    alert("The minimum amount for payment is $0.50.");
                    return; // Prevent submission if the amount is less than $0.50
                }

                // Disable submit button and show loading spinner
                $("#submit").attr("disabled", true);
                $("#button-text").text("Processing...");
                $("#loading-spinner").removeClass("d-none");

                let form = $(this);
                let formData = form.serialize();
                formData += '&package=' + encodeURIComponent(document.getElementById('package').value);
                formData += '&plan=' + encodeURIComponent(document.getElementById('plan').value);
                formData += '&amount=' + encodeURIComponent(document.getElementById('amount').value);
                formData += '&currency=' + encodeURIComponent(document.getElementById('currency').value);
                $.ajax({
                    url: "{{ route('stripe.paymentIntent.seo') }}",
                    method: "POST",
                    data: formData,
                    headers: {
                        "X-CSRF-TOKEN": "{{ csrf_token() }}"
                    },
                    success: function(response) {
                        if (response.success) {
                            stripe.confirmCardPayment(response.clientSecret, {
                                payment_method: {
                                    card: card,
                                    billing_details: {
                                        name: $("input[name=first_name]").val() + " " +
                                            $("input[name=last_name]").val(),
                                        email: $("input[name=email]").val(),
                                    }
                                }
                            }).then(function(result) {
                                if (result.error) {
                                    $("#card-errors").text(result.error.message);
                                    $("#submit").attr("disabled", false);
                                    $("#button-text").text("Pay Now");
                                    $("#loading-spinner").addClass("d-none");
                                } else {
                                    if (result.paymentIntent.status === 'succeeded') {
                                        // Send the payment ID to the backend to update the payment status
                                        $.ajax({
                                            url: "{{ route('stripe.updatePaymentStatus') }}", // Define this route to update the status
                                            method: "POST",
                                            data: {
                                                payment_id: response.payment_id,
                                                status: 'succeeded',
                                                _token: "{{ csrf_token() }}"
                                            },
                                            success: function(response) {
                                                if (response.success) {
                                                    window.location.href =
                                                        "{{ route('stripe.success') }}";
                                                } else {
                                                    alert(
                                                        "Failed to update payment status."
                                                        );
                                                }
                                            }
                                        });
                                    }
                                }
                            });


                        } else {
                            alert("⚠️ Backend error: " + response.message);
                            $("#submit").attr("disabled", false);
                            $("#button-text").text("Pay Now");
                            $("#loading-spinner").addClass("d-none");
                        }
                    },
                    error: function(err) {
                        console.error(err);
                        alert("❌ Something went wrong!");
                        $("#submit").attr("disabled", false);
                        $("#button-text").text("Pay Now");
                        $("#loading-spinner").addClass("d-none");
                    }
                });
            });
        });
    </script>

